#include <iostream>
using namespace  std;

int main(void)
{	
	int N = 0, t1, t2, a[4] = {2,3,5,8}, b[4] = {1,4,6,7};
	for(int i = 0;i < 2*3*4; i++)
	{
		for(int j = 0;j < 2*3*4; j++)
		{
			t1 = (a[0]*10+b[0]) * (a[0]*10+b[0]) + (a[1]*10+b[1]) * (a[1]*10+b[1]) + \
			     (a[2]*10+b[2]) * (a[2]*10+b[2]) + (a[3]*10+b[3]) * (a[3]*10+b[3]); 
			t2 = (b[0]*10+a[0]) * (b[0]*10+a[0]) + (b[1]*10+a[1]) * (b[1]*10+a[1]) + \
			     (b[2]*10+a[2]) * (b[2]*10+a[2]) + (b[3]*10+a[3]) * (b[3]*10+a[3]); 
			if(t1 == t2)
			{
				cout << a[0]*10 + b[0] << "^2 + " << a[1]*10 + b[1] << "^2 + "\
					 << a[2]*10 + b[2] << "^2 + " << a[3]*10 + b[3] << "^2 = " << t1 << endl\
					 << b[0]*10 + a[0] << "^2 + " << b[1]*10 + a[1] << "^2 + "\
					 << b[2]*10 + a[2] << "^2 + " << b[3]*10 + a[3] << "^2 = " << t2 << endl << endl;
				N++;
			}
			b[j%4] ^= b[(j+1)%4],b[(j+1)%4] ^= b[j%4],b[j%4] ^= b[(j+1)%4];
		}
		a[i%4] ^= a[(i+1)%4],a[(i+1)%4] ^= a[i%4],a[i%4] ^= a[(i+1)%4];
	}
	cout << N;
	return 0;
}
